# [3] 붕어빵 클래스 만들기
class 붕어빵 :
    def __init__( self , 내용물 ):
        self.내용물 = 내용물
# [4] 붕어빵 객체 만들기
print( 붕어빵( '팥' ) )   # <__main__.붕어빵 object at 0x00000272E058DD00>
print( 붕어빵( '크림') )   # <__main__.붕어빵 object at 0x00000272E058DD00>
붕어빵1 = 붕어빵('팥')
붕어빵2 = 붕어빵('크림')
