print( 붕어빵2 )       # <__main__.붕어빵 object at 0x00000272E058DDC0>

# [5] 자동차 클래스 만들기
class Car :
    # 생성자
    def __init__( self , carNum ):
        self.carNum = carNum
    # 함수
    def move( self ):
        print( f'{ self.carNum} 가 이동한다.')

# [6] 자동차 객체 생성
유재석차 = Car( '1234' ) # 1234 차량번호의 차 객체를 생성
강호동차 = Car( '4567' ) # 4567 차량번호의 차 객체를 생성
    # 두 객체는 같다고 할수 있을까? 다르다.
print( 유재석차 )   # <__main__.Car object at 0x000001C66018E330>
print( 강호동차 )   # <__main__.Car object at 0x000001C66018E360>
    # 어떻게 다른데???
유재석차.move() # 유재석차(1234) 가 이동 하면 강호동 차는 가만히 있는다.
