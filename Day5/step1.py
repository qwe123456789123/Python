# 조건문 실습2 : 변수와 조건문의 활용
    # 3개의 정수를 입력받아 내림차순 으로 출력하시오.
# 큰값이 앞으로 오는 정렬방식 = 내림차순 , 작은값이 앞으로 오는 정렬방식 : 오름차순
if a < b : # 만약에 첫번째 값이 두번째 값보다 작으면
    a , b = b , a # 두 변수 간의 값 교체 ( 스왑 )
if a < c :
    a , c = c , a
if b < c :
    b , c = c , b
print( f'{ a } , { b } , { c }')

# 스왑 : 두 변수 간의 값 바꾸기
    # 고려 : 변수는 하나의 값만 저장 , 컴퓨터는 순차 처리한다. ( 주면서 받기 X )
var1 = 3
var2 = 5
print( f'{ var1 } , { var2 }')  # 3 , 5
#
# var2 = var1 # var1 의 값을 var2 에 대입하면 var2에 있던 값이 사라짐.
# print( f'{ var1 } , { var2 }')  #   3   3  , 5가 사라짐
#(스왑방법1) 임시변수 temp : 두 변수간의 스왑할때 임시로 값 저장하고 있는 변수
temp = var1
var1 = var2
var2 = temp
print( f'{ var1 } , { var2 }')  # 5 , 3
#(스왑방법2)
var1 , var2 = var2 , var1
print( f'{ var1 } , { var2 }')  # 3 , 5
