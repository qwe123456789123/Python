# 조건문 실습1 : 변수와 조건문의 활용
    # 3개의 정수를 입력받아 가장 큰수 를 출력하시오.

# (1) 입력받아 각 변수에 저장
a = int( input() ) # int( 자료 ) : 정수 타입으로 반환 함수
b = int( input() ) # input() : 입력받은 자료를 문자열로 반환 함수
c = int( input() )
# (2) 가장 큰수를 저장 하는 변수를 임의로 생성 , 왜? 저장/기억 할려고
max = a  # 첫번째 입력받은 값을 가장 큰수로 대입 ,
# (3) 비교
if max < b : # 만약에 두번째 값이 max 보다 크면
    max = b # max에 두번째 값을 대입
if max < c : # 만약에 세번째 값이 max 보다 크면
    max = c # max에 세번째 값을 대입
print( f' max = { max }')
